frinds = ["Apple","Orange", 5 ,345,98,False,"Noob"]

print(frinds[0])

frinds[0] = "Bannana"  # Unlike list in C, Python lists are mutable
print(frinds[4])    
print(frinds[1:4])  # Slicing the list from index 1 to 3
print(frinds[1:])   # Slicing the list from index 1 to the end
print(frinds[:3])   # Slicing the list from the start to index 2    